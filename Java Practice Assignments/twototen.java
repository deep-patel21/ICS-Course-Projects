public class twototen{
  public static void main(String[] args) {
 
    int sum = 0; //declaring counter
    //starting for loop to initiate the counting
    for(int i = -11; i <= -4; i++) { //loop adds 1 to the value of 1 each step from 2 to ten
      sum = sum + i; //adding each updated value to the sum
  
    System.out.println(i); //printing the i value until the loop is complete
    }
 }
}
